from typing import Any, Text, Dict, List
from rasa_sdk import Action, Tracker
from rasa_sdk.executor import CollectingDispatcher
from handlers.weather_handler import get_weather_summary

class ActionGetWeather(Action):

    def name(self) -> Text:
        return "action_get_weather"

    def run(self, dispatcher: CollectingDispatcher,
            tracker: Tracker,
            domain: Dict[Text, Any]) -> List[Dict[Text, Any]]:

        location = tracker.get_slot("location")
        if not location:
            dispatcher.utter_message(text="I'm not sure which location you mean.")
            return []

        try:
            weather = get_weather_summary(location)
            dispatcher.utter_message(text=weather)
        except Exception:
            dispatcher.utter_message(text="Sorry, I couldn't retrieve the weather right now.")

        return []
